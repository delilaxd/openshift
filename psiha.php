<p>
1) <b>Efekat Romea i Julije</b><br><br>
Efekat Romea i Julije predstavlja pojavu da privlačnost između dveju osoba raste, ako postoji protivljenje porodica njihovoj vezi i težnja da se oni razdvoje. To znači da ako roditelji otvoreno krenu da brane nečiju vezu, tada se dvoje mladih još više zaljube jedno u drugo. Mladi ljudi, a posebno tinejdžeri, imaju potrebnu za nezavisnošću i slobodom, i stoga odbijaju da im neko nameće pravila. 
Međutim, treba imati u vidu, da zaljubljivanje, kao rezultat ovog efekta, nije hir i gluma, već se dvoje mladih odistinski više zaljube jedno u drugo. Zato roditelji, nemojte da rizikujete i otvoreno pokazujete svoju netrpeljivost prema drugoj strani.

<br><br>2) <b>Efekat postmatrača</b><br><br>
Efekat posmatrača ukazuje na pojavu da što što je veći broj ljudi koji posmatra neku nesreću, manja je verovatnoća da će neko priteći u pomoć. Što je veći broj prisutnih posmatrača, manje će ljudi biti spremno da pomogne, jer će svi da misle da će neko drugi da priskoči. Ovaj efekat je prilično poznat u nauci. Stoga kada primetite da je nekome potrebna pomoć, nemojte da čekate da još neko reaguje.

<br><br>3) <b>Efekat kiselog grožđa</b><br><br>
Efekat opisuje tendenciju da kada nam je nešto teško dostupno, onda to manje i želimo da postignemo. Privlačniji su nam ciljevi za koje znamo da možemo da ih ostvarimo. Prilično je čest, mada ga ljudi često nisu svesni.

<br><br>4) <b>Pigmalion efekat</b><br><br>
Pigmalion efekat se drugačije naziva i efekat autosugestije. Predrasude i očekivanja od neke osobe se, na kraju, zaista i ostvare. Na primer, predrasude nastavnika, često utiču na uspeh dece. Što se više deca hvale kao uspešna i pametna, ona zaista i budu takva. Naravno, dešava se i obrnuto. Eksperimeni vršeni među američkim osnovcima, na primer, su dokazali, da ako se deca prosečne inteligencije, hvale kao ona sa natprosečnom, ona s vremenom, počnu da pokazuju natprosečne rezultate.

<br><br>5) <b>Zeigarnik efekat</b><br><br>
Ljudi su više skloni da pamte nedovršene događaje ili aktivnosti na kojima su stali do pola. Kada neka aktivnost nije završena, onda se u ljudima stvara napetost, koja uzrokuje da im te aktivnosti bolje ostanu u sećanju. Studenti, koji često prekidaju učenje nevezanim akivnostima, kao što je igranje igrica, bolje pamte. Ovaj trik često koriste  serije, koje se često prekidaju kod najzanimljivijih scena.

<br><br>6) <b>Halo efekat</b><br><br>
Češće poznat kao efekat prvog utiska, halo efekat objašnjava pojavu da ljudi često donose sud o nečijem karakteru na osnovu sveopštog (prvog) utiska. Zato često smatramo da osobe koje prijatno izgledaju, ili su lepo obučene, imaju dobar karakter, iako ove dve stvari realno nemaju veze.

<br><br>7) <b>Efekat publike</b><br><br>
Ljudsko ponašanje često zavisi i od prisutne publike, iako je ta publika pasivna. Ovaj efekat se uvek mora uzimati u obzir prilikom eksperimenata. Ljudi se drugačije ponašaju u zavisnosti od toga da li su sami, među nepoznatim osobama, poznanicima, muškarcima, ženama ili slično. Na primer, muškarcima se menja ponašanje ako se u prisustvu nalaze privlačne žene, radnici se drugačije ponašaju kada šef nije tu i slično. Sledeći put kada ne možete da objasnite svoje ponašanje na nekom događaju, prisetite se ko je sve bio prisutan. Možda je to dovoljno objašnjenje.
